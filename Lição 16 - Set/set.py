# Set: Coleção de valores não-ordenados, não-indexados, e não-duplicados

utensilios = {"Garfo", "Colher", "Faca"}

# Imprimindo os sets
print(utensilios)
print()

# Utilizando Funções no Set
utensilios.add("Guardanapo")
utensilios.remove("Garfo")
utensilios.clear()

# Imprimindo os sets separadamente
for x in utensilios:
    print(x)
print()




