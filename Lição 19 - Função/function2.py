
# Utilizando variaveis dentro da função
def hello(nome):
    print("Olá " + nome)
    print("Tenha um ótimo dia")
# Perceba que usamos a variavel que será usada como parâmetro na função, e dentro do print para ser usada quando chamada.


# Realizando a chamada da função com o nome como parâmetro
