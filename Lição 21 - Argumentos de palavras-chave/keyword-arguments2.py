# Utilizando palavras chaves para os argumentos

def hello(primeiro, meio, ultimo):
    print("Olá " + primeiro + " " + meio + " " + ultimo)


# Especificando cada valor com os parâmetros
# perceba que se especificarmos os valores com os devidos parametros, não importa a ordem, seguirá oq inserido por parâmetro.
hello(ultimo='Neto', meio='Silva', primeiro='Jubileu')
